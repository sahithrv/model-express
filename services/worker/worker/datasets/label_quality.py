from __future__ import annotations

from datetime import datetime, timezone


SUPPORTED_AUDIT_TYPES = {"label_noise_audit", "hard_example_audit"}


def build_label_quality_profile_patch(config: dict, profile: dict | None) -> dict:
    profile = profile if isinstance(profile, dict) else {}
    audit_type = str(config.get("audit_type") or config.get("mechanism") or "").lower()
    if audit_type not in SUPPORTED_AUDIT_TYPES:
        supported = ", ".join(sorted(SUPPORTED_AUDIT_TYPES))
        raise ValueError(f"Unsupported label quality audit_type '{audit_type}'. Supported values: {supported}.")

    audit_record = {
        "schema_version": "label_quality_audit_v1",
        "audit_type": audit_type,
        "status": "completed",
        "report_only": True,
        "created_at": datetime.now(timezone.utc).isoformat(),
        "plan_id": str(config.get("plan_id") or ""),
        "dataset_id": str(config.get("dataset_id") or ""),
        "experiment_index": config.get("experiment_index"),
        "target_metric": str(config.get("target_metric") or ""),
        "mechanism": str(config.get("mechanism") or audit_type),
        "intervention": str(config.get("intervention") or ""),
        "evidence_used": _string_list(config.get("evidence_used")),
        "expected_effect": str(config.get("expected_effect") or ""),
        "summary": _audit_summary(audit_type, profile),
        "findings": _audit_findings(audit_type, profile),
        "candidate_examples": _candidate_examples(profile),
        "notes": "Report-only worker audit; no labels, files, splits, or dataset records were mutated.",
    }
    existing_audits = profile.get("label_quality_audits")
    audits = existing_audits if isinstance(existing_audits, list) else []
    artifacts = profile.get("artifacts")
    artifact_records = artifacts if isinstance(artifacts, list) else []
    return {
        **profile,
        "label_quality_audit": audit_record,
        "label_quality_audits": [audit_record, *audits][:10],
        "artifacts": [
            *artifact_records,
            {
                "artifact_type": "label_quality_audit",
                "path": "",
                "format": "profile_json",
                "description": f"Report-only {audit_type} generated by the worker.",
            },
        ][:200],
    }


def _audit_summary(audit_type: str, profile: dict) -> dict:
    class_distribution = profile.get("class_distribution")
    if not isinstance(class_distribution, dict):
        class_distribution = profile.get("images_per_class") if isinstance(profile.get("images_per_class"), dict) else {}
    counts = [int(value) for value in class_distribution.values() if _is_int_like(value)]
    minority_count = min(counts) if counts else 0
    majority_count = max(counts) if counts else 0
    return {
        "audit_type": audit_type,
        "class_count": int(profile.get("class_count") or len(class_distribution) or 0),
        "image_count": int(profile.get("total_images") or profile.get("image_count") or 0),
        "imbalance_ratio": float(profile.get("imbalance_ratio") or 0.0),
        "minority_class_count": minority_count,
        "majority_class_count": majority_count,
        "corrupt_file_count": int(profile.get("corrupt_file_count") or profile.get("corrupt_image_count") or 0),
        "leakage_warning_count": len(profile.get("leakage_warnings") or []),
    }


def _audit_findings(audit_type: str, profile: dict) -> list[dict]:
    findings: list[dict] = []
    class_distribution = profile.get("class_distribution")
    if not isinstance(class_distribution, dict):
        class_distribution = profile.get("images_per_class") if isinstance(profile.get("images_per_class"), dict) else {}
    if class_distribution:
        counts = {str(label): int(count) for label, count in class_distribution.items() if _is_int_like(count)}
        if counts:
            median_count = sorted(counts.values())[len(counts) // 2]
            for label, count in sorted(counts.items(), key=lambda item: item[1])[:10]:
                if count < max(2, median_count * 0.5):
                    findings.append(
                        {
                            "kind": "minority_class_review",
                            "class_name": label,
                            "support": count,
                            "reason": "Class has low support relative to the dataset median.",
                        }
                    )
    for warning in profile.get("leakage_warnings") or []:
        findings.append({"kind": "leakage_warning", "reason": str(warning)})
    for image_path in profile.get("corrupt_images") or []:
        findings.append({"kind": "corrupt_image", "path": str(image_path), "reason": "Profiler could not read this image."})
    visual_traits = profile.get("visual_trait_summary") if isinstance(profile.get("visual_trait_summary"), dict) else {}
    if audit_type == "hard_example_audit":
        for key in ("blur_likelihood", "background_dominance", "lighting_variation", "object_scale"):
            value = str(visual_traits.get(key) or "")
            if value in {"high", "small"}:
                findings.append({"kind": "visual_trait_review", "trait": key, "value": value})
    return findings[:50]


def _candidate_examples(profile: dict) -> list[dict]:
    candidates = []
    for key in ("visual_exemplars", "demo_images"):
        values = profile.get(key)
        if not isinstance(values, list):
            continue
        for item in values:
            if not isinstance(item, dict):
                continue
            candidates.append(
                {
                    "source": key,
                    "path": str(item.get("path") or item.get("relative_path") or ""),
                    "class_name": str(item.get("class_name") or item.get("label") or ""),
                    "reason": "Existing profile exemplar available for human review.",
                }
            )
            if len(candidates) >= 50:
                return candidates
    return candidates


def _string_list(value: object) -> list[str]:
    if isinstance(value, list):
        return [str(item) for item in value]
    if value is None:
        return []
    return [str(value)]


def _is_int_like(value: object) -> bool:
    try:
        int(value)
        return True
    except (TypeError, ValueError):
        return False
